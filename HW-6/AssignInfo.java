
public class AssignInfo {

	private String assignResult;
	private String assignMessage;
	private Course course;
	
	public String getAssignResult() {
		return assignResult;
	}
	public void setAssignResult(String assignResult) {
		this.assignResult = assignResult;
	}
	public String getAssignMessage() {
		return assignMessage;
	}
	public void setAssignMessage(String assignMessage) {
		this.assignMessage = assignMessage;
	}
	public Course getCourse() {
		return course;
	}
	public void setCourse(Course course) {
		this.course = course;
	}
	
	
	
	
}
